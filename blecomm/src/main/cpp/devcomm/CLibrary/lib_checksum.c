uint8 LibChecksum_GetChecksum8Bit
(
	const uint8 *u8p_Data,
	uint16 u16_Length
)
{
	uint8 u8_Index;
	uint8 u8_Checksum;


	u8_Checksum = 0x00;

	while (u16_Length--)
	{
		u8_Index = *u8p_Data++ ^ u8_Checksum;
		u8_Checksum = m_u8_ChecksumTable8Bit[u8_Index];
	}

	return u8_Checksum;
}


