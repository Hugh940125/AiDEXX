uint16 LibChecksum_GetChecksum16Bit_CCITT
(
	const uint8 *u8p_Data,
	uint16 u16_Length
)
{
	uint16 crc = 0xFFFF;
	uint16 polynomial = 0x1021;

	for (int j = 0; j < u16_Length; ++j)
	{
		for (int i = 0; i < 8; i++)
		{
			char bit = ((u8p_Data[j] >> (7 - i) & 1) == 1);
			char c15 = ((crc >> 15 & 1) == 1);
			crc <<= 1;
			if (c15 ^ bit)
				crc ^= polynomial;
		}
	}

	return crc;
}

uint16 LibChecksum_GetChecksum16Bit
(
	const uint8 *u8p_Data,
	uint16 u16_Length
)
{
	uint8 u8_Index;
	uint16 u16_Checksum;


	u16_Checksum = 0xFFFF;

	while (u16_Length--)
	{
#if LIB_CHECKSUM_16_BIT_COMPACT != 0
		u8_Index = ((uint8)(u16_Checksum) & 0x0F) ^ (*u8p_Data & 0x0F);
		u16_Checksum >>= 4; 
		u16_Checksum ^= m_u16_ChecksumTable16Bit[u8_Index]; 
		u8_Index = ((uint8)(u16_Checksum) & 0x0F) ^ (*u8p_Data >> 4);
		u16_Checksum >>= 4; 
		u16_Checksum ^= m_u16_ChecksumTable16Bit[u8_Index]; 
		u8p_Data++;
#else
		u8_Index = *u8p_Data++ ^ u16_Checksum;
		u16_Checksum >>= 8;
		u16_Checksum ^= m_u16_ChecksumTable16Bit[u8_Index];
#endif
	}

	return u16_Checksum;
}


